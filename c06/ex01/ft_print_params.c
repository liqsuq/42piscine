#include <unistd.h>

void	ft_putstr(char *str)
{
	char	*cur;

	cur = str;
	while (*cur)
	{
		write(1, cur, 1);
		cur++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	while (i < argc)
	{
		ft_putstr(argv[i]);
		i++;
	}
	return (0);
}
